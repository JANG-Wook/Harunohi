/** 아이콘 + 전경색 + 배경색 → img src 로 쓰는 data-URI SVG. iconKey 미지정/미존재 시 robot. */
export function buildProfileAvatar(iconKey, bgColor = '#EAF1FF', iconColor = '#0066FF') {
  const fg = (ICON_FG[iconKey] ?? ICON_FG.robot).replaceAll('{C}', iconColor)
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 80 80"><rect width="80" height="80" fill="${bgColor}"/>${fg}</svg>`
  return `data:image/svg+xml,${encodeURIComponent(svg)}`
}
